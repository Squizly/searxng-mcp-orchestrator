DECISION_PROMPT = """Ты решаешь, нужны ли дополнительные поисковые запросы.
Исходный запрос: "{user_input}"

Правила:
- Если вопрос простой и прямой, дополнительных запросов не нужно.
- Если вопрос сложный, неоднозначный или требует контекста, нужны дополнительные запросы.
- Если нужны дополнительные запросы, сгенерируй ровно 3 точных и разноплановых запроса на том же языке.
- Если не нужны, верни пустой список.
- Ответ должен быть только JSON-объект следующего вида:
{{
  "use_extra_queries": true/false,
  "queries": ["...", "...", "..."]
}}
Никаких пояснений, кода или текста вне JSON.
"""

SUMMARY_PROMPT = """Ты суммируешь результаты веб-поиска.
Запрос пользователя: "{query}"

Результаты:
{results}

Требования к ответу:
- Используй только факты из результатов, не выдумывай.
- Если данных недостаточно, прямо скажи об этом.
- Если результаты не соответствуют запросу, ответь: "Результаты не соответствуют запросу."
- Не перечисляй каждый результат по отдельности — дай связный ответ.
- Пиши на языке запроса, без смеси языков.
- Пиши грамотно, без орфографических ошибок.
- Ответ должен быть кратким: 4–6 предложений, до ~1000 символов.
- Не добавляй токсичные/травмирующие/насильственные детали, если пользователь их не просил.
- Не добавляй список источников — он будет добавлен отдельно.
"""


def build_decision_prompt(user_input: str) -> str:
    return DECISION_PROMPT.format(user_input=user_input)


def build_summary_prompt(query: str, results: str) -> str:
    return SUMMARY_PROMPT.format(query=query, results=results)
